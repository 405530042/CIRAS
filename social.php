		<p>第三個轉向則是在生命歷程的時間構成性與社區工作和治理的空間構成性(被簡化為城鄉差距)間，朝向人類互動是發生在時間和空間相互構成性的社會本體論中(Social Ontology)。跨越實證論式簡化的規律性因果概念，在積累性的因果和結構化機制作用的方法論中，我們不僅要認識「時間」的多重性構成和限制作用，從「地方空間」在社區工作和治理研究，以至於社會/老人學中(包括晚近地理老人學到社會地理學，Skinner, et al., 2018；Del Casino,2013)的發展，我們也發現實證論式單面向思考和修正式分離互動思考模式的不足，以及時間和空間相互構成的緊要性(表三)。從社會本體論回到社會科學方法論的重構，在我多年社會科學方法論的教學經驗中，如何從關注實證論式知識的最終確定基礎(Foundationalism, Knowledge from What?)，轉向實用主義式提問「知識的作用」(Kowledge for What)的發展，二十一世紀社會科學正逐步揮別尋找最終實在的形上學和激進相對主義化的社會建構論，而強調社會動態過程的時間化理論和順序(相對於生命事件史)分析技術發展(Abbott, 2016)，有助我們回到認識社會本身的實踐、互動和信任構成。</p>

		<p>
			回顧2003年底，當我還沉浸在政治哲學的理念探索中，被夥伴們一把拉進正在掙扎、奮力更新的社區發展和福利社區化政策，幫助他們理解新興文化部門的社區營造及其社區(群)主義的「新理念」。經歷了<span style="font-weight:bold">開創期(2003-2008)</span>間執行教育部三年期的「建立台灣社會福利工作人才養成模式實驗計畫」，<span style="font-weight:bold">建置期(2008-2012)</span>先後成立了<a href="http://www1.pu.edu.tw/~TACS/start.html">台灣社區工作與社區研究學會</a>(2008) (會員網絡包括金門、澎湖離島在內共21個校系所)和發行<a href="http://www1.pu.edu.tw/~TACS/news.html">台灣社區工作與社區研究學刊</a>(2010)(已出版至七卷一期)，到成熟期(2012-2016)執行衛福部二年期的「全國社區育成中心計畫」(共同主持人，改由吳明儒主持)，主編出版第六卷第二期的《社區培力專刊》(2016年8月)，並發表理論性批判「社會的」概念及其缺席論文：〈從<a href="https://www.google.com.tw/url?sa=t&rct=j&q=&esrc=s&source=web&cd=2&cad=rja&uact=8&ved=0ahUKEwiWzubuk5_XAhWJS7wKHal4A-8QFggvMAE&url=http%3A%2F%2Ftci.ncl.edu.tw%2Fcgi-bin%2Fgs32%2Fgsweb.cgi%3Fo%3Ddnclresource%26s%3Did%3D%2522A16025136%2522.%26searchmode%3Dbasic%26tcihsspage%3Dtcisearch_opt1_search&usg=AOvVaw0nR3SWSNtnoYpbyo1dqz0Z">「社區發展」到「社會企業」？關於社會企業的政策議程和政治想像</a>〉(2016)，致力於帶動我國(續社區發展和社區營造之後)「第三波社區工作運動」的發展和成熟。當前籠罩在長照2.0下的社區照顧議題，當然是我們必須且已加緊仔細檢視和批判工作。

		<p><span style="font-weight:bold">我們是不是應該再上路了? 夥伴們!</span> 實現一個留給我們子孫「有希望的未來」的平凡夢想</p>

		<p>長年社區工作實務的接觸和經驗，讓我深深體會社區工作者的愚人精神，樂於分享和共享。他們不僅要有孤單行走的準備，也必須面對各種名利和權力的誘惑與腐蝕，更得認識到各種搭便車參與者的袖手旁觀和成果掠奪心態。幸好，總是會在路途中碰到其他愚人夥伴同行外，也有不少機會在眾多搭便車者中感召轉化成為新夥伴或參與者。在各種拚湊不成的深耕計畫難產後，個人對於被徵召提出深耕計畫的自我調適，就是謹守著社區工作者追求共善的「志業」(Vocation)信念，沒有汲汲營營的事業，戮力於重新打造學校、學術和國家的共同體理念、分享與夥伴精神。而我們時代的任務很清楚，就是留給我們子孫一個「有希望的未來」，特別是當這個未來早已經被新自由主義、拼裝的第三條路和全球金融資本主義扭曲到難以辨識，留下「沒有其他選項」的困境。在舊夥伴的陪同和新夥伴的加入下，我們正式提出這項(雙重)轉向計畫。</p>

		<p>「轉向」不僅是為了消極地減少「好點子不斷地淪為壞主意」的困境。更重要的實質任務，除了在社會實務上用來做為推動「社區治理」的基本知識和「社區實務工作」行動參考框架(見下圖)，也致力於推動當代社會科學的知識及其作用的反思性重建。透過社會批判和知識批判的雙向作用，以及透過社會實踐產生的反思性認識和試誤修正，「轉向」將帶動實用主義的社會知識論和政治倫理學，矯正當代社會科學過度經驗和應用取向的盲點，並警告我們實用的知識不(只/會)是知識的盲目應用。</p>


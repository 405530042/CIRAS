		<p>我們採行四種相甫的執行策略歸納說明如下: (一) 建置/分析量化資料庫和視覺化系統，(二) 思考轉向的交流學習/逐步聚焦，(三)在地化社區工作和治理的實作服務及操作模式實驗，與(四)國際政策社群的多層面/多層次社會學習。</p>

		<p><span style="font-weight:bold">策略一：資料庫的建置和視覺化分析</span>。表面上這只是個高度理論化的計畫，但實際上卻是建立在台灣社區工作實務過程和社會的生命歷程發展趨勢、現象及後果的社會診斷性認識上。因為沒有理論的經驗研究是盲目的，沒有經驗研究的理論是空洞的。我們首先面臨的就是如何透過生命歷程事件史的趨勢，來認識台灣的人口地層位移和人口海嘯造成的社會災害，包括第二個兒童期和高齡島嶼化現象的形成。而這會是個長期艱鉅的任務，即使幾任的政務委員承諾和努力下，也依舊是個不可及的資料庫建置工程，畢竟不只是燒錢就可以做到。話雖然這麼說，但沒有錢就更加辦不到。在財力微薄卻沒有時間可以等待之下，我們選擇就是用熱情和信念出發了，如同十年前僅用人社圖書計畫的學校補助款三十萬，我們帶動了台灣社區工作的GIS教學和運用。</p>

		<p>夥伴們聚會討論整晚後，我歸納步驟如下。在打開這黑箱式的社會生命歷程動態過程時，我們同時需要一個完整、但尚未存在的<span style="font-weight:bold">台灣生命歷程事件史趨勢資料庫</span>，以及<span style="font-weight:bold">視覺化社會科學分析技術系統</span>的協助，完成<span style="font-weight:bold">看到台灣</span>的計畫。資料分析團隊的兩項主要任務：第一項Mapping Tawian系列：1) Ageing Taiwan (鄉鎮層級資料的老化)(如附件一)；2) 社區台灣Community in Taiwan和3) Health and Health Care Taiwan。後續包括教育程度、勞動率、家庭組成和居住安排的地圖化。當然，後者有<a href="https://www.google.com.tw/url?sa=t&rct=j&q=&esrc=s&source=web&cd=1&cad=rja&uact=8&ved=0ahUKEwiPxr7KoJ_XAhUM5bwKHZAQC8MQFggmMAA&url=http%3A%2F%2Fwww.nhri.org.tw%2F&usg=AOvVaw3rZyObeU_1xfHFuoJ8Ju4X">國家健康研究院</a>的公衛觀點計劃支持，但仍舊不同於我們生命歷程觀點的認識。第二項台灣生命歷程事件史趨勢圖，規劃納入前述八項現代社會重要人生事件的平均年齡和不同年齡的發生率資料庫之後，我們將繼續朝向當前視覺化社會科學技術的前沿走去。</p>

		<p>然而正在發展中的視覺化社會科學分析系統，也受到財務能力的嚴重限制，蓬勃的想像與願景仍在繼續匍匐前進。我國的健康研究和公衛系統大概是少數可以有較多財務支持的領域，也已經能開始投入資源和人力進行初步建置工作，見附件三。如何透過與健康和公衛研究現有成果連結(Jones, 2017)，然後給予理論生命和政策視野的擴張，會是這個計畫打開沒錢、少人的第一步。落實我的理論想像和擘劃，需要公衛的李妙純教授作為連結任務的節點，而本計畫資料庫和分析的主舵手必須是社會科學少有資訊能手的陳昭榮講師。最後，我認為剛起步的視覺化分析技術，其實仍然有待理論所期待的突破性考驗：從簡易、片斷化的圖示和分析作用，朝向主題式<span style="font-weight:bold">整體觀點</span>的(空間性)關聯建構顯示後，還能夠帶出多向度(個體主觀、客觀、社會和制度)時間層次<span style="font-weight:bold">動態作用</span>的發展過程。</p>


		<center><h1>研究方法</h1></center>

		<p>在邁向「高齡社會，完整人生」研究的三維向度中，我們首先以社區工作與治理作為初步整合的場域和學習性交流平台架構(見圖四)。而在現有規劃的<span style="font-weight:bold">社會轉型</span>計畫中，我們的<span style="font-weight:bold">研究策略</span>採取哲學理論組、制度政策組、方法論經驗研究組，和組織管理社區服務組共四個界線鬆散的研究群組(見圖五)，或獨立或合作或團隊模式，進行探索轉向思考與思考轉向的相關議題。在落實到研究執行層次，我們則採取四種相甫的<span style="font-weight:bold">執行策略</span>。</p>


			<img src="images/social/Gantt.png">
		</div>

		<p><span style="font-weight:bold">策略二：讓思考轉向來帶動轉向的思考和社會參與行動</span>。相對於理論和經驗研究的斷裂，我們首要是讓理論和高度量化資料有真正的對話和啟蒙。同樣的，制度政策研究和社區服務行動與組織管理，也會跟著我們一起由思考的轉向，帶動轉向的思考和執行社區工作引導的行動。</p>

		<p>然而，任何整合性研究都面臨著內部暨外部的整合而非整人問題。在尊重學科專業的前提下，透過「高齡社會，完整人生」，以及要給子孫們一個「有希望的未來」理念共享下，計畫採行團隊研究、網絡學習和成果社會參與的三向度策略，透過逐步聚焦和形成更適切的研究問題。目前，除了社會政策和社工福利服務、哲學和政治理論學者，我們還有來自法律、勞動、經濟、成人教育、台文所社造學者和會資所的社會企業等新夥伴的參與，同時也是集合教授、副教授和助理教授的進行世代傳承與營造合作關係的契機(參見附件的高齡跨域創新研究中心社會轉型組研究社群名單)。我們將依需要，逐年邀請提出和執行研究計畫。</p>
					

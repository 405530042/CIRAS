		<p><span style="font-weight:bold">策略三：走進台灣的社區，推動在地化的生活、生產及生態之永續化</span>。Robert Bellah等美國社群主義學者(1985)早就提醒現代社會生態的高度個人主義災害，腐蝕人際間和世代間的共同連結與民主的公共生活，危害程度與後果將甚於自然災害的力量。超過十年的社區工作實務和組織經營，做為衛福部社區發展業務的重要夥伴，我們不僅從事治標性的生活照顧服務，更透過治本性的共同生產模式(co-production)來活化地方產業和生態營造，讓成年工作人口回流和在地生活的在地化發展，健全化地方人口年齡結構的根基與前提下，發展社區化的老幼共生照顧圈。地圖台灣：「社區台灣」盤點、建立和監控我國社區生活的主要面貌、變動趨勢和影響因素，包括國家政策與地方政府應有和實際作為的累積性因果影響。配合著「社區工作與治理」計畫的執行，選定蹲點式的社區場域據點，分成北中南東及離島，五個分區建立偵測基地，逐年收集資料進行社區老化歷程之分析。</p>

		<p><span style="font-weight:bold">策略四：國際政策社群的多層面/多層次社會學習</span>。成立於2009年日本東京大學的老人學研究機構(IOG東京大学高齢社会総合研究機構)，結合社區治理的社區工作高齡服務模式，的確是我們所規劃完整人生高齡社會研究的一部分，但顯然的，絕對不是本計畫的全部和總目標。相對的，德國、瑞士和歐盟的國家及跨國研究計畫和研究中心，才是更符合做為我們學習的標竿，但它們既有的資料系統建置和國家資源投入，和我們天差地遠。與其自不量力的選擇這些先進機構，三項分組計畫的協商結議，我們先行以IOG做為短期的標竿。但我們會以德國和歐盟的研究目標、策略選擇、組織模式和運作方法做為長期標竿來邁進。同時，需要再重複強調的，與其選擇一個靜態、單一化的容易標竿去前進，我們需要的是能帶動整體社會制度改革的動態化學習過程：從表層參考移植到深層理念的自我發展。</p>

		<h1>預期完成工作項目及成果</h1>

		<p>本計畫採行團隊研究、網絡學習和成果社會參與的三維向度策略，透過逐步聚焦和形成更適切的研究問題。也很清楚地設定了三向度的內涵與工作項目。團隊研究包括有分全國五區在地社區參與團隊、研究計畫及其成果報告、以及資料庫建置和視覺化分析團隊(地圖台灣組和台灣生命歷程事件史趨勢組)。網絡學習則從讀書會、學習坊(理論/方法)、主辦全國會議，並在辦理國際會議外，鼓勵和支持國際會議參與，也定期出訪國際主要研究中心學習。最後，在成果的社會參與中，除了在地社區參與團隊的社區工作具體成果，我們以出版論文、期刊書籍、政策建議報告書和建立常設、有責信的研究中心，積極為打造全人、全程和全民的「妥善的」社會制度，努力集眾人之分享和共善力量，實現一個留給我們子孫「有希望的未來」的平凡夢想。</p>


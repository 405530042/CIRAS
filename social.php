		<p>第二個轉向要提出一種哲學式理論研究和制度政策分析的共同生產和夥伴關係，探索經濟永續性、社會構成性、政治正當性、文化心理穩定性的多層面框架，建構一個「妥善的」，全人、全程和全民的社會暨福利體制(表一)，具體地回應困擾著當代世界的工作貧窮、不穩定的低度就業和世襲化的不平等。主流「以年齡群組」做分類和規劃(含蘊年齡歧視Ageism)的社會福利政策與「市場化社會」的政治經濟體制正面臨著「財政赤字」、「人口赤字」和「照顧赤字」的多重複雜難題(見表一)，並進一步走向掏空公民參與和制度信任的「民主赤字」。但這絕對不只是個政策創新、經驗研究或直接提供服務的單純問題。對「理念」及其政治道德心理動員力量的使用和濫用，是西方現代性文明的核心力量所在，無論是自由、平等或正義、權利。</p>

		<p>二十世紀下半政治哲學和倫理學的復興腳步，從讚揚到質疑John Rawls的正義理論(1971； 2001)和重分配正義的社會正義原則，我們逐步的學習到隱藏在其中的分類區隔、不平等體制、宰制關係和邊緣化策略。自由的平等主義面臨到被理(或誤)解為放任市場結果後再進行事後重分配的差異原則(劣勢者救助)。重分配正義的策略性失敗外，為回應指責而更常以工作倫理和個人責任的論證(Ronald Dworkin)來支持財產權和消費者主權觀的個人選擇與責任，高度忽略社會經濟不平等對工作貧窮和不穩定低薪就業間的惡性循環(Standing, 2017)，並帶來性別秩序的失調和更龐大集體認同要求團體加入追求肯認政治的新社會運動(Honneth, 2014)。經歷了1990年代不平等惡化和全球化的資本主義擴張，Rawls最終提出了財產所有制的民主(Property-Owning Democracy,POD)概念，試圖抗衡福利資本主義所造成的不平等。羅爾斯主義者紛紛沿著路線走，轉向前分配(Pre-distribution)的概念，並從哲學的殿堂走下來，加入了制度設計和政策辯論的陣營(O'Neill and Williamson, 2012；張世雄2016；Thomas, 2017；Van Parijs and Vanderborght, 2017))。相對的，社會政策與福利國家研究者則多半顯得抗拒，或者止於高談人類權利和公民地位，卻未能深究其隱藏的意涵和概念陷阱。這個倫理和政治轉向，對於當前高齡社會的討論最是迫切，無論是Workfare State, Activation State, Social Investment State，或是Basic Income, Basic Capital和資產為本平等主義的社會改革主張和制度重組建議。這裡提出一個生命歷程體制層級的分析架構(表二)，來連結人類發展條件、制度通道設計和政治倫理的反思，批判性地分析和檢視一個全人、全程和全民的社會福利體制，不能僅有生產性和消費性的功能(如歐洲社會模式或社會投資國家)，且必須是社會性的構成基石和政治正當性的穩定來源。</p>

		<p>最近研究，當前社會福利理論的基本範疇和重要爭議(2014-2016)和檢視當代不平等和關懷倫理的社會福利理論議題: 從正義出發的質問(2016-2018)兩項科技部計畫，當然給了我和已經聽了三年課博士生們莫大的幫助。哲學系、思想史研究和政治理論的夥伴們也給予豐富的討論與對話機會，讓我在對理論絕緣的社會福利封閉圈子內，打開另一扇知識的大門，重新看待三百年來社會福利、民主政治和工業與資本主義的人類發展歷史。</p>


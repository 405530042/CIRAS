						<h1>高齡生理、心理、認知與運動健康研究群</h1><br>
						<center><h1>計畫內容概述</h1></center>

  		<p>今日的高齡研究不再將65歲以上的高齡人口視為同一個族群，而是依其年齡層概分為三組：輕高齡（65-74歲）、高高齡（75-84歲）、超高齡（85歲以上）。「活躍老化」（Active Aging）的概念強調高齡者的身心健康、參與和安全達到最適化，並促進其生活品質，其中高齡者生理與心理認知狀態的維持可說是高齡者自主生活並維持其生活品質的一切基礎。我們可以將高齡人口依其身心健康狀況略分為：健康（有一至二項慢性疾病，能自理生活）、亞健康（慢性疾病及輕度失能，在他人協助下能維持生活）、失能（喪失大部份生活能力，需全天看護）等三個類別，目前在台灣，三個類別的比例約為7：2：1。而隨著高齡人口的增長，高高齡與超高齡族群人口比例增加，也意味著亞健康乃至失能的人數及人口比例必然隨之增加，因此如何評斷高齡者生理及心理認知能力的退化程度並發展早期檢測技術，提供預防性的認知訓練，以延長其健康生活狀態的研究工作，也就有其重要性與急迫性。</p>

		<p>在如何延緩生心理老化的討論中，運動可說是其中的關鍵。就生理方面來說，每天規律性的運動可保持個人的肌耐力，同時可加強心肺及循環系統功能並促進新陳代謝，對於延緩人體組織與器官的老化有相當之助益。而近年來在許多的運動實證研究中也可以發現體適能訓練一方面除可以促進心肺功能的提升，更可因為循環系統效能的增進，進而增加大腦的血液流量，讓腦細胞獲得更多的養分，從而降低大腦細胞氧化速度；而跑步運動的實證研究更顯示跑步可促進腦細胞生長，有助於記憶力提高、學習能力提升與減緩老化性智力衰退。這些研究都顯示了適當強度的規律性運動，對於高齡者的身心健康促進將有莫大之助益。</p>

		<p>綜觀整個老化過程中，生理、心理與認知功能之間是相互受到影響的，彼此在長期交互作用之下則會出現社會老化，社會老化意指在生理與心理的老化之後，將使得個人與社會之間的互動及關係產生改變，如受到體力下降影響，參與社會活動的欲望降低、生活安於習慣、態度趨於保守等情況出現。因此在針對高齡者的研究討論中，除了生理/心理/認知研究之外，也應該當重視個人與社區、團體之間的互動情況，如何從個人的環境背景中梳理出地方文化、社群認同等脈絡，創造出適合高齡者參與的環境，如此才有助於個人在老化過程中重拾正面態度，充實生活內涵以追求完整的人生。</p>

		<p>因此本小組的研究規劃，將分為四個研究群：「生理臨床基礎研究群」、「心理認知基礎研究群」、「運動與健康促進研究群」及「地方文化價值與社會認知研究群」。透過四個研究群同步運作，我們將以生理/心理/認知/運動科學等基礎研究為開始，以瞭解並尊重在地社會文化脈絡為前提，鼓勵高齡者以積極正面態度參與社區活動，並以基礎研究成果為偏鄉高齡者提供適當的治療與預防課程，同時導入合宜之生活及照護科技，協助社區高齡居民維持自主日常生活並持續進行社會參與。</p>


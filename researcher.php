		<h1>(一)生理臨床基礎研究</h1>

		<p>人類在老化的過程中需面對器官機能與認知能力退化、免疫力與抵抗力下降的情況，這些都是高齡者維持其身心健康狀況所必然面臨的情況。因此當高齡者罹患疾病時，常出現多重病因的情況，表現出非典型病徵、療效差、復原慢以及癒後活動力衰退等現象。因此關於高齡者疾病之基礎醫學研究、照護技術提升和早期篩檢之議題乃為本研究群工作的第一個研究重點。研究群的另一項重要研究課題將著眼於高齡失智議題，其中阿茲海默症是最常見的失智症之一，目前全球大約有3到4千萬人口罹患阿茲海默症，保守估計到了2050年人數將再增加3倍。我們將以高齡者臨床及基礎醫學研究為開端，進行疾病、神經退化、感染議題研究，同時希望由影像醫學、生理、基因與認知研究多管齊下，探究失智早期診斷指標及篩檢機制，最後與「心理認知研究群」、區域醫療系統、研究單位及生醫產業合作，發展與評估失智早篩技術。在研究實務規劃上，我們將從四個主題方向進行：</p>

		<p><span style="font-weight:bold">1.銀髮臨床與基礎醫學研究</span></p>

		<p>近年來科學家已發現腸道菌群組成可能與中樞神經疾病之發生有密切關聯。研究結果顯示腸道菌可能透過腸腦軸(gut-brain axis)的作用而影響認知功能。目前國內外已有研究團隊證實阿茲海默症及帕金森氏症與腸道菌群之關聯性。故找出有神經退化疾病之台灣人腸胃道菌群的區域性差別，並依此做為臨床應用的基礎研究，其重要性不言可喻。因此本研究將針對神經退化的病人，進行腸道菌相基礎分析，了解腸道菌相與親緣、族群、飲食、疾病、用藥之關聯性。同時，我們亦將藉由腸胃道菌群之調控，觀察改變腸胃道菌群與菌相，是否能夠改善藥物治療的效果，進而達到客製化精密醫學的目標。</p>

		<p><span style="font-weight:bold">3.早期失智篩檢關鍵技術研發</span></p>

		<p>阿茲海默症的發生目前被認為與神經細胞堆積異常的「乙型澱粉樣蛋白(beta-amyloid protein)」並形成斑塊，導致發炎與「濤(tau)蛋白」磷酸化，進而產生神經纖維的糾結與壞死有關。過去科學家們普遍認為，只要解決乙型澱粉樣蛋白的異常堆積，就能克服阿茲海默症，但後來發現，即使去除腦內異常堆積蛋白，也不代表患者的記憶與痴呆情況可以獲得改善。阿茲海默症可能要長達20年才會出現症狀，現在的診斷方法只能在阿茲海默症晚期病人已明顯損失認知能力時才能診斷出這種疾病，再加上失智症有好幾種形式，使得診斷更加複雜。因此如能透過早期診斷，在病患尚未受到無可挽回的傷害前，便施以藥物治療，或許能減緩或防止疾病的進程，這也突顯早期篩檢技術研發的重要性。我們對失智早篩研究重點如下: </p>
		<p>(1)探討阿茲海默症生物標記（biomarker）之可應用性:包括基因突變、「tau蛋白」含量、骨質疏鬆關聯性、乙醯膽鹼（acetylcholine）含量、視網膜細胞檢查。<br>
		(2)正子造影系統（positron emission tomography,PET）顯影技術之評估，並據乙型澱粉樣蛋白在腦中聚集及分布的情形，進行疾病進展及細胞分子層次的影響評估。<br>
		(3)臨床、生物標記、影像醫學及遺傳醫學之失智資訊整合。<br>
		(4)與鄰近區域醫療機構合作，推廣早期失智篩檢門診。</p>

	<p><span>4.照護機構管理與感染控制研究</span></p>

		<p>有鑑於高齡人口之免疫與抵抗能力下降，長期照護機構如遭受帶抗藥性之致病菌感染，將對居住於內的高齡者健康產生極大影響。抗藥性致病菌可能來自於高齡者在醫院與照護機構間往返導致，亦可能因為長期照護機構的環境衛生出現問題，或是透過照護人員或探望家屬交互感染給住民。我們將整合各國研究成果，針對地方長照中心常見之五類帶抗藥性致病菌ESKAPE (Enterococcus faecium, Staphylococcus aureus, Klebsiella pneumoniae, Acinetobacter baumannii, Pseudomonas aeruginosa, Enterobacter spp.)進行研究，了解上述五類致病菌在醫院、居家及長照中心之多重抗藥性現況，並從流行病學的觀點探究長期照護機構的環境監控議題，為地方公衛主管機關，醫療院所提供疾病防治與公衛建議。除此之外，對於病原菌即時監控之檢測儀器研發及疫病雲端管理系統之運作與改進，亦是本小組與醫療院所及照護機構未來推行之工作重點。</p>

		<p><span style="font-weight:bold">2.以人體消化道微生物相分析進行中樞神經退化疾病之精密醫學研究</span></p>

		<p>本中心將參考IOG之整體運作模式，以基礎研究與科技研發做為基底，學術工作者將其知識與理論轉為行動研究，特別是以生活試驗室連結產官學，走入社區與在地居民共同合作之理念與作法，將是本中心的未來主要之運作模式。藉由<span style="font-weight:bold">高齡社區共創生活實驗室</span>此一平台，<span style="font-weight:bold">與產官學研、社區居民及在地組織</span>，攜手進行學術研究、技術研發、人才培育、社會服務、國際合作，以科技為輔助，以教育為策略觀點，透過社區資源整合，社區全民投入，輔以大學研究與輔導諮詢為引導，一同解決雲嘉嘉地區超高齡社會問題，<span style="font-weight:bold">共創臺灣超高齡社會因應新模式</span>，再透過全國跨區域大學實踐聯盟與高齡跨域國際產學聯盟的籌組，將此新創模式推廣到更多地方。</p>

		<p>東京大學高齡社會綜合研究機構作為日本乃至於世界上著名的高齡研究機構，本中心期待於5年內達到該機構之規模與成就，雖有其困難處，然而積極作為就是轉機。近年來跨國比較研究興起，「在地化」研究越深入則越容易「國際化」，如能貫徹「在地老化」理念以及結合本校基礎研究、科技研發與社區工作多年的成果，本中心有信心5年後將能建立具臺灣特色的「在地老化」高齡研究模式，並以此為開端，嘗試推動臺灣與日本乃至東亞地區的跨國比較研究，讓本校的「高齡跨域創新研究中心」成為國際上想瞭解臺灣高齡研究現況最佳選擇。</p>

